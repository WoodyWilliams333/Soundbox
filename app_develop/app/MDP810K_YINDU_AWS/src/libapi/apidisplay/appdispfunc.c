static void ShowBorder(int x, int y, uint8_t mode)
{
    if (mode) {
        app_lcd_draw_bmp(x, y, black_border);
    } else {
        app_lcd_draw_bmp(x, y, white_border);
    }
}

void disp_nfc_led(appbool blue, appbool yellow, appbool green, appbool red){
	(blue == app_true) ? (ShowBorder(120, 20, 1)) : (ShowBorder(120, 20, 0));
	(yellow == app_true) ? (ShowBorder(120, 20+12, 1)) : (ShowBorder(120, 20+12, 0));
	(green == app_true) ? (ShowBorder(120, 20+12*2, 1)) : (ShowBorder(120, 20+12*2, 0));
	(red == app_true) ? (ShowBorder(120, 20+12*3, 1)) : (ShowBorder(120, 20+12*3, 0));
}


