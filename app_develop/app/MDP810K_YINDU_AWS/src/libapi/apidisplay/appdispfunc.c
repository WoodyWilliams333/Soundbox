void disp_mul_lan_string(int Col, int Line, uint32_t Mode, 
							const char *pstChBuf, const char *pstEnBuf)
{
	if( App_Lcd_Lan == APP_ZH ){
		app_lcd_display(Col, Line, Mode, "%s", pstChBuf);		
	}
	else{
		app_lcd_display(Col, Line, Mode, "%s", pstEnBuf);		
	}
}

void disp_title(const char *pstChBuf, const char *pstEnBuf)
{
    if (App_Lcd_Lan == APP_ZH) {
		app_lcd_display(0, 0, DISP_CFONT|DISP_MEDIACY|DISP_INVLINE, "%s", pstChBuf);		
    } else {
        app_lcd_display(0, 0, DISP_CFONT|DISP_MEDIACY|DISP_INVLINE, "%s", pstEnBuf);
    }
}


